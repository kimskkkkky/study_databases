  ADD CONSTRAINT FK_CARS_TO_NEW_ID
    FOREIGN KEY (CARS_ID)
    REFERENCES CARS (CARS_ID);

ALTER TABLE NEW_ID
  ADD CONSTRAINT FK_CARS_OPTION_TO_NEW_ID
    FOREIGN KEY (CARS_OPTION_ID)
    REFERENCES CARS_OPTION (CARS_OPTION_ID);

ALTER TABLE CARS
  ADD CONSTRAINT FK_CARS_CODE_TO_CARS
    FOREIGN KEY (CARS_CODE_ID)
    REFERENCES CARS_CODE (CARS_CODE_ID);

ALTER TABLE CARS
  ADD CONSTRAINT FK_COMPANY_CODE_TO_CARS
    FOREIGN KEY (COMPANY_CODE_ID)
    REFERENCES COMPANY_CODE (COMPANY_CODE_ID);

ALTER TABLE CARS
  ADD CONSTRAINT FK_AGE_CODE_TO_CARS
    FOREIGN KEY (AGE_CODE_ID)
    REFERENCES AGE_CODE (AGE_CODE_ID);
